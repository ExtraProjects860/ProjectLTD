
class EdgeDiary {
    constructor() {
        this.travelDate = null;
        this.start = null;
        this.warehouse = null;
        this.factory = null;
        this.horse = null;
        this.cart = null;
    }

    getTravelDate() {
        return this.travelDate;
    }

    getStart() {
        return this.start;
    }

    getWarehouse() {
        return this.warehouse;
    }

    getFactory() {
        return this.factory;
    }

    getHorse() {
        return this.horse;
    }

    getCart() {
        return this.cart;
    }
}